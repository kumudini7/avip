from __future__ import annotations

from typing import Any

_COMPLEXITY_PAYBACK_MONTHS = {
    "low": 6,
    "low-medium": 8,
    "medium": 10,
    "medium-high": 12,
    "high": 14,
}

_CATEGORY_AUTO_PCT = {"pure_rpa": 75, "rpa_ai": 65, "rpa_ui": 70}
_CATEGORY_SAVING_MULTIPLIER = {"pure_rpa": 5, "rpa_ai": 7, "rpa_ui": 4}


def _benchmarks(
    category: str,
    complexity: str,
    *,
    auto_pct: int | None = None,
    time_saved_mins: int | None = None,
    payback_months: int | None = None,
    saving_multiplier: int | None = None,
) -> dict[str, int]:
    return {
        "auto_processing_pct": auto_pct if auto_pct is not None else _CATEGORY_AUTO_PCT[category],
        "time_saved_mins": time_saved_mins if time_saved_mins is not None else 30,
        "payback_months": payback_months if payback_months is not None else _COMPLEXITY_PAYBACK_MONTHS.get(complexity, 10),
        "annual_saving_multiplier": saving_multiplier if saving_multiplier is not None else _CATEGORY_SAVING_MULTIPLIER[category],
    }


def _pad_value_props(explicit: list[str], systems: list[str]) -> list[str]:
    result = list(explicit)
    extras = [
        f"Integrates cleanly with {', '.join(systems[:2])}" if systems else "Integrates cleanly with existing systems",
        "Reduces manual errors through consistent, rule-based execution",
    ]
    for extra in extras:
        if len(result) >= 5:
            break
        result.append(extra)
    return result[:5]


def _entry(
    *,
    domain: str,
    category: str,
    title: str,
    overview: str,
    process_flow: list[str],
    systems_involved: list[str],
    complexity: str,
    value_props: list[str],
    auto_pct: int | None = None,
    time_saved_mins: int | None = None,
    payback_months: int | None = None,
    saving_multiplier: int | None = None,
) -> dict[str, Any]:
    return {
        "domain": domain,
        "category": category,
        "title": title,
        "overview": overview,
        "process_flow": process_flow,
        "systems_involved": systems_involved,
        "roi_benchmarks": _benchmarks(
            category,
            complexity,
            auto_pct=auto_pct,
            time_saved_mins=time_saved_mins,
            payback_months=payback_months,
            saving_multiplier=saving_multiplier,
        ),
        "value_props": _pad_value_props(value_props, systems_involved),
        "complexity": complexity,
    }


